def clean_text(text):
    if not text: return ""
    replacements = {"أ": "ا", "إ": "ا", "آ": "ا", "ة": "ه", "ى": "ي", " ": ""}
    for old, new in replacements.items():
        text = text.replace(old, new)
    return text.strip()

